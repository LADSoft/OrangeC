{
    int top = size;
    int bottom = -1;
//    int v;
    while (top - bottom > 1)
    {
        int mid = (top + bottom) / 2;
        if (val < table[mid])
        {
            top = mid;
        }
        else
        {
            bottom = mid;
        }
    }
    if (table[bottom] != val )
        return 0;
    return 1;
}
